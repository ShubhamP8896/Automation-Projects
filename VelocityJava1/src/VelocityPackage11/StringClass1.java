package VelocityPackage11;

public class StringClass1                      //Session 31
{
	public static void main(String[] args) {
		
//	Char concept
	
	char a = 'S';
	char b = 'H';
	char c = 'U';
	char d = 'B';
	char e = 'Z';
	
	System.out.println(a);
	System.out.println(b);
	System.out.println(c);
	System.out.println(d);
	System.out.println(e);
	
//	String Concept
	
	String m = "SHUBZ PAWAR";
	System.out.println(m);
	
//	Alpha Numeric Values :- Combination of all the charectores, symbols and numbers
	
	String p = "Shubz@881996";
	System.out.println(p);
	
//	Syntax  :- String - Literal
	String s = "Shubham";
	System.out.println(s);
	
//	Syntax : String - new keyword
	
	String r = new String ("Pawar");
	System.out.println(r);
	
	
			
	
	}
	

}
